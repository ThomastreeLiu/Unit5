public class Arraypic {
    public static void main (String args[]){
        String duck[] = {"Yellow","Yellow","Black","Yellow","Yellow",
                "Yellow"};
        String dice[] = {"Cow","Crocodile","Paw print","Wheel","Jellyfish",
        "Sloth","Swan"};
        String scarbble[] = {"O1","D2","S1","W4","R1","T1"};
        String pvz[] = {"Sunflower","Pea shooter","Sunflower","Ice archer"};
        String square[] = {"Red smile","White","Grey","Red","Red smile"};
        String fruit[] = {"Acorn","Banana","Plums","Plums","Pears","Apple",
                "Apple","Acorn"};
        for (int i = 0;i<duck.length;i++){
            System.out.println(duck[i]);
        }
        for (int i = 0;i<dice.length;i++){
            System.out.println(dice[i]);
        }
        for (int i = 0;i<scarbble.length;i++){
            System.out.println(scarbble[i]);
        }
        for (int i = 0;i<pvz.length;i++){
            System.out.println(pvz[i]);
        }
        for (int i = 0;i<square.length;i++){
            System.out.println(square[i]);
        }
        for (int i = 0;i<fruit.length;i++){
            System.out.println(fruit[i]);
        }
    }
}
